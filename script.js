/* =========================================================
   Referências do DOM e estado da interface
   ========================================================= */
const navbar = document.querySelector('.navbar');
const sections = [...document.querySelectorAll('section')];
const navLinks = [...document.querySelectorAll('.nav-links a')];
const scrollContainers = [...document.querySelectorAll('.section-scroll')];
const heroSection = document.getElementById('home');
const langToggle = document.getElementById('langToggle');
const i18nNodes = [...document.querySelectorAll('[data-i18n]')];
const metaDescription = document.querySelector('meta[name="description"]');
const mobileViewport = window.matchMedia('(max-width: 767px)');

let activeSection = document.querySelector('section.active') || heroSection;
let resizeTimer = null;
let navigationLocked = false;
let heroHasPlayed = false;
let currentLanguage = 'pt';

/* =========================================================
   Helpers
   ========================================================= */
function updateNavbarOffset() {
  if (!navbar) {
    return;
  }

  const navHeight = Math.ceil(navbar.getBoundingClientRect().height);
  document.documentElement.style.setProperty('--nav-offset', `${navHeight}px`);
}

function getTranslationValue(path, locale) {
  return path.split('.').reduce((accumulator, key) => accumulator?.[key], translations[locale]);
}

function applyLanguage(locale) {
  const selectedLocale = translations[locale] ? locale : 'pt';
  const labels = translations[selectedLocale].aria;

  currentLanguage = selectedLocale;
  document.documentElement.lang = translations[selectedLocale].documentLang;

  if (metaDescription) {
    metaDescription.setAttribute('content', translations[selectedLocale].metaDescription);
  }

  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const value = getTranslationValue(key, selectedLocale);

    if (typeof value === 'string') {
      node.textContent = value;
    }
  });

  heroSection?.setAttribute('aria-label', labels.home);
  document.getElementById('about')?.setAttribute('aria-label', labels.about);
  document.getElementById('services')?.setAttribute('aria-label', labels.services);
  document.getElementById('works')?.setAttribute('aria-label', labels.works);

  if (langToggle) {
    langToggle.dataset.lang = selectedLocale;
    langToggle.setAttribute('aria-label', labels.langToggle);
  }
}

function setActiveLink(sectionId) {
  navLinks.forEach((link) => {
    const isActive = link.getAttribute('href') === `#${sectionId}`;

    link.classList.toggle('active', isActive);
    link.setAttribute('aria-current', isActive ? 'page' : 'false');
  });
}

function sectionNeedsScrollbar(section) {
  const scroller = section?.querySelector('.section-scroll');

  if (!scroller) {
    return false;
  }

  return scroller.scrollHeight - scroller.clientHeight > 6;
}

function updateScrollbar(section) {
  if (!section || mobileViewport.matches) {
    return;
  }

  const scroller = section.querySelector('.section-scroll');
  const scrollUi = section.querySelector('.scroll-ui');
  const thumb = section.querySelector('.scroll-thumb');

  if (!scroller || !scrollUi || !thumb) {
    return;
  }

  if (!sectionNeedsScrollbar(section)) {
    scrollUi.classList.add('is-hidden');
    thumb.style.transform = 'translate3d(0, 0, 0)';
    return;
  }

  const maxScroll = scroller.scrollHeight - scroller.clientHeight;
  const trackHeight = scrollUi.clientHeight;
  const ratio = scroller.clientHeight / scroller.scrollHeight;
  const thumbHeight = Math.max(trackHeight * ratio, 40);
  const maxThumbY = trackHeight - thumbHeight;
  const progress = maxScroll > 0 ? scroller.scrollTop / maxScroll : 0;
  const y = maxThumbY * progress;

  scrollUi.classList.remove('is-hidden');
  thumb.style.height = `${thumbHeight}px`;
  thumb.style.transform = `translate3d(0, ${y}px, 0)`;
}

function updateAllScrollbars() {
  if (mobileViewport.matches) {
    return;
  }

  sections.forEach(updateScrollbar);
}

function resetReveals(section) {
  section.querySelectorAll('.section-reveal').forEach((element) => {
    element.style.transition = 'none';
    element.style.opacity = '0';
    element.style.transform = 'translateY(12px) translateZ(0)';
  });

  requestAnimationFrame(() => {
    section.querySelectorAll('.section-reveal').forEach((element) => {
      element.style.transition = '';
      element.style.opacity = '';
      element.style.transform = '';
    });
  });
}

/* =========================================================
   Animação do hero
   ========================================================= */
function playHeroIntro(force = false) {
  if (!heroSection) {
    return;
  }

  const heroContent = heroSection.querySelector('.hero-content');

  if (!heroContent || (heroHasPlayed && !force)) {
    return;
  }

  const children = heroContent.querySelectorAll('*');

  if (mobileViewport.matches) {
    heroContent.classList.remove('is-mobile-intro');

    children.forEach((item) => {
      item.style.animation = 'none';
    });

    void heroContent.offsetWidth;

    children.forEach((item) => {
      item.style.animation = '';
    });

    heroContent.classList.add('is-mobile-intro');
    heroHasPlayed = true;
    return;
  }

  heroContent.style.animation = 'none';

  children.forEach((item) => {
    item.style.animation = 'none';
  });

  requestAnimationFrame(() => {
    heroContent.style.animation = '';

    children.forEach((item) => {
      item.style.animation = '';
    });

    heroHasPlayed = true;
  });
}

/* =========================================================
   Navegação entre seções
   ========================================================= */
function openSection(id, options = {}) {
  const target = document.getElementById(id) || heroSection;

  if (!target || target === activeSection || navigationLocked) {
    return;
  }

  navigationLocked = true;

  if (activeSection) {
    activeSection.classList.remove('active');

    const previousScroller = activeSection.querySelector('.section-scroll');
    if (previousScroller) {
      previousScroller.scrollTop = 0;
    }
  }

  target.classList.add('active');
  activeSection = target;

  setActiveLink(target.id);
  resetReveals(target);

  if (target.id === 'home') {
    playHeroIntro(options.replayHero === true);
  }

  requestAnimationFrame(() => {
    updateScrollbar(target);
  });

  window.setTimeout(() => {
    navigationLocked = false;
  }, mobileViewport.matches ? 240 : 420);
}

/* =========================================================
   Eventos
   ========================================================= */
if (langToggle) {
  langToggle.addEventListener('click', () => {
    const nextLanguage = currentLanguage === 'pt' ? 'en' : 'pt';

    applyLanguage(nextLanguage);
    updateNavbarOffset();
  });
}

scrollContainers.forEach((scroller) => {
  const section = scroller.closest('section');
  let ticking = false;

  scroller.addEventListener(
    'scroll',
    () => {
      if (mobileViewport.matches || ticking) {
        return;
      }

      ticking = true;

      requestAnimationFrame(() => {
        updateScrollbar(section);
        ticking = false;
      });
    },
    { passive: true }
  );
});

navLinks.forEach((link) => {
  link.addEventListener('click', (event) => {
    event.preventDefault();

    const id = link.getAttribute('href').replace('#', '');

    history.replaceState(null, '', `#${id}`);
    openSection(id, { replayHero: true });
  });
});

window.addEventListener('hashchange', () => {
  const id = (location.hash || '#home').replace('#', '');
  openSection(id, { replayHero: false });
});

window.addEventListener(
  'resize',
  () => {
    clearTimeout(resizeTimer);

    resizeTimer = window.setTimeout(() => {
      updateNavbarOffset();
      updateAllScrollbars();
    }, 90);
  },
  { passive: true }
);

window.addEventListener(
  'orientationchange',
  () => {
    window.setTimeout(updateAllScrollbars, 140);
  },
  { passive: true }
);

window.addEventListener('load', () => {
  const initialId = (location.hash || '#home').replace('#', '');
  const targetSection = document.getElementById(initialId) || heroSection;

  applyLanguage('pt');
  updateNavbarOffset();

  sections.forEach((section) => {
    section.classList.remove('active');
  });

  targetSection.classList.add('active');
  activeSection = targetSection;

  setActiveLink(targetSection.id);
  updateAllScrollbars();

  window.setTimeout(updateAllScrollbars, 120);

  if (targetSection.id === 'home') {
    playHeroIntro(true);
  }
});
